#include "libft.h"

/** PURPOSE : returns 1 if space or variation. */
t_bool	ft_isspaces(int c)
{
	return (c == ' ' || c == '\t' || c == '\n' || \
	c == '\r' || c == '\f' || c == '\v');
}
